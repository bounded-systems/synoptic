#!/usr/bin/env node
// synoptic gen-palette-page <tokens.css> [--out palette.html] — render a palette reference
// page from a tokens.css: each color as a swatch + token name + OKLCh + per-axis name
// (axis-name) + hex. A VIEW of the palette in the grounded form, not a new design system.
// The page dogfoods the palette itself (ground + text drawn from its own colors).
import { readFileSync, writeFileSync } from "node:fs";
import { canonicalizeTyped } from "../canonicalize.mjs";
import { axisName } from "../axis-name.mjs";
const src = process.argv[2] || "tokens.css";
const outI = process.argv.indexOf("--out"); const out = outI >= 0 ? process.argv[outI + 1] : "palette.html";
const css = readFileSync(src, "utf8");
const esc = (s) => String(s).replace(/&/g, "&amp;").replace(/</g, "&lt;");
const cols = [...css.matchAll(/--(color-[a-z0-9-]+|grade-[a-z0-9-]+|ink-[a-z0-9-]+)\s*(?::|\{[^}]*initial-value:)\s*(#[0-9A-Fa-f]{6})\b/g)]
  .filter((m, i, a) => a.findIndex((x) => x[1] === m[1]) === i)
  .map((m) => { const t = canonicalizeTyped(m[2]); return { name: m[1], hex: m[2].toUpperCase(), t, axis: axisName(t) }; });
const group = (pred) => cols.filter(pred);
const groups = [
  ["Brand", group((c) => /forest|ink|paper|card|white|line/.test(c.name) && !/grade/.test(c.name))],
  ["Accents", group((c) => /clay|amber|on-forest/.test(c.name))],
  ["Grades", group((c) => /grade/.test(c.name))],
];
const card = (c) => `<figure class="sw">
  <div class="chip" style="background:${c.hex}"></div>
  <figcaption><b>--${esc(c.name)}</b>
    <span class="axis">${esc(c.axis.name)}</span>
    <span class="ok">oklch(${c.t.l}% ${c.t.c} ${c.t.h})</span>
    <span class="hex">${c.hex}</span></figcaption></figure>`;
const ground = cols.find((c) => c.name === "color-ink")?.hex || "#16221C";
const text = cols.find((c) => c.name === "color-on-forest")?.hex || "#D2E0D8";
const line = cols.find((c) => c.name === "color-line")?.hex || "#888374";
const html = `<!doctype html>
<html lang="en"><head><meta charset="utf-8"><meta name="viewport" content="width=device-width,initial-scale=1">
<title>Palette</title>
<meta name="description" content="The design-system palette, each color in OKLCh with its per-axis name.">
<style>
:root{--g:${ground};--t:${text};--l:${line};--mono:ui-monospace,"IBM Plex Mono",monospace;--disp:"Space Grotesk",system-ui,sans-serif}
*{box-sizing:border-box}body{margin:0;background:var(--g);color:var(--t);font-family:var(--disp);line-height:1.5}
.wrap{max-width:64rem;margin:0 auto;padding:clamp(1.5rem,5vw,4rem)}
header{border-bottom:1px solid var(--l);padding-bottom:1.5rem;margin-bottom:2rem}
h1{font-weight:600;font-size:clamp(2rem,7vw,3.2rem);letter-spacing:-.02em;margin:0}
.sub{opacity:.7;font-family:var(--mono);font-size:.8rem;margin:.6rem 0 0}
h2{font-family:var(--mono);font-size:.75rem;letter-spacing:.18em;text-transform:uppercase;opacity:.6;margin:2.5rem 0 1rem}
.grid{display:grid;grid-template-columns:repeat(auto-fill,minmax(13rem,1fr));gap:1rem}
.sw{margin:0;border:1px solid var(--l);border-radius:12px;overflow:hidden;background:rgba(128,128,128,.06)}
.chip{height:5.5rem}
figcaption{padding:.7rem .8rem;display:flex;flex-direction:column;gap:.15rem;font-family:var(--mono);font-size:.72rem}
figcaption b{font-weight:400;color:var(--t)}
.axis{opacity:.95;font-family:var(--disp);font-size:.8rem;margin:.1rem 0}
.ok{opacity:.65}.hex{opacity:.45;letter-spacing:.04em}
footer{margin-top:3rem;border-top:1px solid var(--l);padding-top:1.2rem;font-family:var(--mono);font-size:.72rem;opacity:.6}
</style></head><body><div class="wrap">
<header><h1>Palette</h1><p class="sub">${cols.length} tokens · each color in OKLCh + its per-axis name (hue · Value · Chroma% of gamut)</p></header>
${groups.filter(([, g]) => g.length).map(([t, g]) => `<h2>${t}</h2><div class="grid">${g.map(card).join("")}</div>`).join("")}
<footer>Generated by <code>synoptic gen-palette-page</code> from the design tokens. Names are per-axis (Munsell-in-oklch), not composite — see <code>spec/color-model.md</code>. Every value is the color's own coordinate.</footer>
</div></body></html>`;
writeFileSync(out, html);
console.log(`palette page → ${out}  (${cols.length} colors, ${groups.filter(([,g])=>g.length).length} groups)`);
